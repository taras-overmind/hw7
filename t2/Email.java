package com.taras.t2;

public class Email {
    public String theme;
    public String from;
    public String to;

    public void setTheme(String theme) {
        this.theme = theme;
    }

    public void setFrom(String from) {
        this.from = from;
    }

    public void setTo(String to) {
        this.to = to;
    }

    public String getTheme() {
        return theme;
    }

    public String getFrom() {
        return from;
    }

    public String getTo() {
        return to;
    }
}
